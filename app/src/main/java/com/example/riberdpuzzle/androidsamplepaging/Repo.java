package com.example.riberdpuzzle.androidsamplepaging;


public class Repo {
    private int id;
    private String name;
    private String url;

    private int getId() {
        return id;
    }

    private void setId(int id) {
        this.id = id;
    }

    private String getName() {
        return name;
    }

    private void setName(String name) {
        this.name = name;
    }

    private String getUrl() {
        return url;
    }

    private void setUrl(String url) {
        this.url = url;
    }
}
